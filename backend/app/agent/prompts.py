# ---------- Score ----------

# 打分维度按轮次分层：技术轮（peer/high_peer）考技术准确性/表达/深度；
# 经理轮（manager）多为开放题，没有技术对错，改考判断力/结构化表达/成熟度。
_TECH_DIMS = {
    "technical": "技术准确性与深度",
    "expression": "表达清晰度、逻辑",
    "depth": "思考深度、举一反三、边界考虑",
}
_MANAGER_DIMS = {
    "insight": "判断力与洞察：观点是否有依据、有辨识度，而非空泛套话或正确的废话",
    "communication": "结构化表达：条理清晰、重点突出、有说服力",
    "seniority": "视野与成熟度：战略视野、自我认知、经验迁移与长期思考",
}
SCORE_DIMENSIONS = {
    "peer": _TECH_DIMS,
    "high_peer": _TECH_DIMS,
    "manager": _MANAGER_DIMS,
}


def build_score_system(role: str) -> str:
    """按轮次角色生成打分 system prompt（不同轮次用不同评分维度）。"""
    dims = SCORE_DIMENSIONS.get(role, _TECH_DIMS)
    dim_lines = "\n".join(f"- {k}: {v}" for k, v in dims.items())
    keys_json = ",".join(f'"{k}":0-10' for k in dims)
    n = len(dims)

    if role == "high_peer":
        round_note = "本轮是二面（资深），可要求系统设计深度与架构权衡，深度不足是实质减分项。"
    elif role == "manager":
        round_note = "本轮是三面（经理），重在判断力、视野与表达；开放题没有标准答案，不要因'没有唯一正确答案'而扣分。"
    else:
        round_note = (
            "本轮是一面（同级 Peer），重点考察实操能力与沟通，不要用资深架构评审的严苛尺子；"
            "思路合理、方向正确即可给中上分，深度/细节不足只算小幅扣分。"
        )

    return (
        "你是面试官，对候选人在本题上的回答进行打分。\n\n"
        "【重要：这是语音面试的转写文本，可能有语音识别错误】\n"
        "- 遇到明显被转写错的术语（如 SaaS→SARS、MCP→MSP、event type→even the type、"
        "block→broke、chunk→创客、Verify→Wifi、insert after→in that afternoon），"
        "按最合理的技术意图理解，绝不因此判为'概念混用/表达混乱'。\n"
        "- 不因口语不流畅、填充词（呃/然后/这个）、口头重复而扣分——那是说话的正常现象，不是能力问题。\n"
        "- 语音面试不要求口述代码或精确的数据结构定义；看设计意图与思路完整性，不要因'没给代码片段'而扣分。\n"
        "- 按回答的实质内容和意图打分，而不是措辞是否漂亮。\n\n"
        f"【本轮定位】{round_note}\n\n"
        f"评分维度（每项 0-10 分）：\n{dim_lines}\n\n"
        "【打分锚点】\n"
        "- 9-10：思路清晰、有具体方案/取舍/依据，超出预期\n"
        "- 7-8：正确且较完整，抓住要点，仅细节欠缺\n"
        "- 5-6：方向对、有合理内容，但不够深入或有小遗漏（口语面试的常见合格线）\n"
        "- 3-4：明显偏题、空泛，或有较大缺陷\n"
        "- 0-2：答错、答非所问或基本没答\n"
        "注意：'方向正确但深度一般'应落在 5-6，而不是 2-3；不要轻易给极端低分。\n\n"
        "只输出 JSON：\n"
        f'{{"dimensions":{{{keys_json}}},"total":0-100,"comment":"150-250 字点评"}}\n\n'
        f"total = round((各维度之和)/{n} * 10, 1)\n\n"
        "【comment 写作要求】\n"
        "1. 先用 1 句话概括总体水平（肯定做到的部分）。\n"
        "2. 针对得分最低的 1-2 个维度，具体说明：\n"
        "   - 回答里哪一段/哪个具体内容让该维度失分（引用候选人原话片段）\n"
        "   - 这道题上'深度/独到见解/结构'不足，具体指的是缺少哪类内容（举例说明应该补什么）\n"
        "3. 给出 1-2 条后续提升建议，要具体可执行（如：'下次回答成功三要素时，建议加一个反例或 trade-off 分析'）。\n"
        "4. 不要泛泛说'深度不足'，要说清楚是什么的深度、缺什么、补什么。\n"
        "5. 语气像资深面试官给建议，不是评判，用中文。"
    )


